FeedingSchedule scheduleForWeeks(int weeks) {
  for (final s in feedingSchedules) {
    if (weeks >= s.ageWeeksMin && weeks <= s.ageWeeksMax) return s;
  }
  return feedingSchedules.last;
}

int weeksSince(String babyBirthDate, {DateTime? now}) {
  final birth = DateTime.tryParse(babyBirthDate);
  if (birth == null) return 0;
  final n = now ?? DateTime.now();
  final days = n.difference(birth).inDays;
  return days < 0 ? 0 : days ~/ 7;
}
